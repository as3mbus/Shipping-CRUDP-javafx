CREATE  TABLE SHIPMENT (
BOOKINGID VARCHAR(7) PRIMARY KEY,
BOOKINGDATE DATE,
BOOKINGFROM VARCHAR(255),
SHIPPERNAME  VARCHAR(255),
SHIPPERADDRESS VARCHAR(255),
CONSIGNEENAME VARCHAR(255),
CONSIGNEEADDRESS VARCHAR(255),
PLACEOFRECEIPT VARCHAR(255),
PORTOFLOADING VARCHAR(255),
VESSELORVOYAGE VARCHAR(255),
VESSELETD DATE,
VESSELETA DATE,
TRANSSHIPMENTPORT VARCHAR(255),
IVESSELORVOYAGE VARCHAR(255),
IVESSELETA DATE,
IVESSELETD DATE,
PORTOFDISCHARGE VARCHAR(255),
DISCHARGEETA DATE,
FINALDESTINATION VARCHAR(255),
CARGOSIZE ENUM( 'TWENTY', 'FORTY' ),
CARGOTYPE ENUM ( 'GP' , 'HC' ),
VOLUMECONT INT,
COMMODITY VARCHAR(255),
FREIGHT ENUM( 'COLLECT' , 'PREPAID' ),
STUFFINGPLACE VARCHAR(255),
STUFFINGDATE DATE
);